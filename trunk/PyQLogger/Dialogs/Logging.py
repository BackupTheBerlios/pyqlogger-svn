class LoggerWidget(QPushButton):
    def __init__(self, parent, logger, owner):
        QPushButton.__init__(self, parent, "")
        self.setText("Log Info")
        self.setToggleButton (True)
        self.owner = owner
        layout = QVBoxLayout(owner.logFrame)
        self.listbox = QListBox ( owner.logFrame )
        layout.addWidget ( self.listbox )
        self.connect(self, SIGNAL("toggled(bool)"), self.toggled )
        self.loghandler = LogHandler( self.listbox , self )
        logger.addHandler( self.loghandler )        
        
    def timerDone( self ):
        self.setText("Log Info")
        
    def newLogAvailable( self ):
        if not str(self.text()) == "Log Info (new)":
            timer = QTimer( self )
            self.connect( timer, SIGNAL("timeout()"), self.timerDone )
            self.setText("Log Info (new)")
            timer.start( 5000, True )

    def toggled (self, ins):
        if ins:
            self.owner.logFrame.show()
        else:
            self.owner.logFrame.hide()
            